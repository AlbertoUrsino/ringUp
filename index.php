                                Il trust è uno strumento giuridico di origine anglosassone ammesso nell’ordinamento
                                italiano
                                a seguito della ratifica nel 1992 della Convenzione dell’Aja che permette di costituire
                                una massa segregata
                                di beni e diritti e vede la presenza di quattro soggetti: il disponente, il trustee, i
                                beneficiari e il guardiano.<br />
                                Il disponente è colui che con atto unilaterale istituisce il trust e ne dispone a favore
                                beni e diritti nominando un trustee.
                                Il trustee è colui a cui il disponente ha conferito il potere di amministrare e gestire
                                i beni e i diritti disposti
                                in trust, secondo le istruzioni ricevute dal disponente ed in vista dello scopo da
                                questi determinato.<br />
                                Il beneficiario è il destinatario finale nel cui interesse vengono gestiti i beni in
                                trust.<br />
                                Il guardiano è colui al quale il disponente può attribuire compiti di controllo e
                                vigilanza dell’operato del trustee
                                e il potere di revoca e sostituzione dello stesso.<br />
                            </p>
                        </div>
