                                L’affidamento fiduciario è un negozio giuridico di recentissima origine dottrinale
                                e interamente basato sull’interazione di principi e articoli enunciati nel Codice Civile
                                supplendone alla lacunosa disciplina (vedasi art. 790 c.c. per donazione con riserva di
                                disporre,
                                art. 784 c.c. per donazione a nascituri) in cui un soggetto, l’affidante, affida a un
                                terzo,
                                l’affidatario che agisce in qualità di fiduciario con vincolanti obblighi giuridici
                                posti a suo carico,
                                un patrimonio segregato con l’eventuale apposizione di un vincolo pubblicitario ex. art.
                                2645 comma 3 c.c.
                                per i beni immobili o i beni mobili registrati, affinché questi ne sviluppi il programma
                                voluto e dettato
                                dall’affidante stesso a vantaggio di uno o più beneficiari.<br />
                                Questo programma deve descrivere sia le funzionalità che i bisogni specifici che devono
                                essere indirizzati ai
                                beneficiari e di cui l’affidatario fiduciario deve tenerne obbligatoriamente conto.
                                Oltre alle necessarie figure
                                dell’affidante, dell’affidatario fiduciario e dei beneficiari spesso si inserisce nel
